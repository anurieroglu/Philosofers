#include "philo.h"

int	check_arg(char **s)
{
	char	**p;

	p = s;
	while (*p)
	{
		if(!ft_isdigit(*p) || ft_atol(*p) < 0 || ft_atol(*p) > INT_MAX)
			return (1);
		++p;
	}
	return (0);
}